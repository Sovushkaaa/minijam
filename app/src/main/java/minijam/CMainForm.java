    private void initCustomComponents() {
        
        Log.print(this, "init custom components.");
        
        //init render
        m_render = new Render();
        m_render.setSize(Settings.WindowSize);
        m_render.setBackground(Settings.RenderColor);
        
        m_render.setLayout(null);
        
        //Axes shape
        CShapeBase shape = new CAxes();
        shape.setSize(new CVector(  Settings.WindowSize.width, 
                                    Settings.WindowSize.height));
        
        shape.setStyle(new CShapeStyle(Color.YELLOW, new Color(0,0,0,0), 2));
        
        m_render.addShape(shape);
        
        //hexagon shape
        CShapeBase hexagon = new Hexagon();
        hexagon.setSize(new CVector(130, 130));        
        hexagon.setPosition(new CVector(900, 440));
        hexagon.setStyle(new CShapeStyle(Color.PINK, Color.WHITE, 1));

        m_render.addShape(hexagon);
        
        //button for hexagon
        JButton btnToggleVisibility = new JButton("Hide Hexagon");
        btnToggleVisibility.setName("btn_hexagon");
        btnToggleVisibility.setBounds(1060, 670, 200, 30);
        btnToggleVisibility.setBackground(Color.white);

        btnToggleVisibility.addActionListener(e -> {
            if (hexagon.getStyle().Color.getAlpha() == 0) {
                hexagon.setStyle(new CShapeStyle(Color.PINK, Color.WHITE, 1));
                btnToggleVisibility.setText("Hide Hexagon");
            } else {
                Color transparent = new Color(0, 0, 0, 0);
                hexagon.setStyle(new CShapeStyle(transparent, transparent, 0));
                btnToggleVisibility.setText("Show Hexagon");
            }
            m_render.repaint();
        });
        
        m_render.add(btnToggleVisibility);
        
        //shape star
        CVector size = new CVector(200, 200);
        CVector size_star = new CVector(70, 70);

        CStar star = new CStar();
        star.setSize(size_star);
        star.setOrigin(new CVector(size_star.x/2, size_star.y/2));
        star.setPosition(new CVector(Settings.WindowSize.width/4, 
                                        Settings.WindowSize.height/2 
                                                + Settings.WindowSize.height/5));
        
        star.setStyle(new CShapeStyle(new Color(135, 220, 125), 
                new Color(75, 120, 75), 3));
        
        m_render.addShape(star);
        CVector size_but = new CVector(200, 30);
                
        JButton btnStar = new JButton("Start animation");
        btnStar.setBounds(   Settings.WindowSize.width/4 - size_but.x/2, 
                                Settings.WindowSize.height 
                                                - Settings.WindowSize.height/8 + size_but.y/2,
                                size_but.x,
                                size_but.y);
        btnStar.setName("btn_star_animation");
        btnStar.setBackground(new Color(135, 220, 125));
        btnStar.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createLineBorder(new Color(75, 120, 75), 5),
            BorderFactory.createEmptyBorder(5, 5, 5, 5)
        ));
        
        btnStar.addActionListener(e -> {
            btnStar.setText(!star.getStateAnimation() ? "Stop animation" : "Start animation");
            star.setStateAnimation(!star.getStateAnimation());
        });
        
        m_render.add(btnStar);
        
        //shape octagon
        
        COctagon octagon = new COctagon();
        octagon.setSize(size);
        octagon.setBaseSize(octagon.getSize());
        octagon.setOrigin(new CVector(size.x/2, size.y/2));
        octagon.setPosition(new CVector(Settings.WindowSize.width/4, 
                                        Settings.WindowSize.height/4));
        
        octagon.setStyle(new CShapeStyle(new Color(51, 51, 51), Color.BLACK, 4));
        
        m_render.addShape(octagon);
        
        //octagon button
        size.x = 200;
        size.y = 30;
                
        JButton btnOctagon = new JButton("Start animation");
        
        btnOctagon.setName("btn_octagon_animation");
        btnOctagon.setBounds(   Settings.WindowSize.width/4 - size.x/2, 
                                Settings.WindowSize.width/4 - size.y/2,
                                size.x,
                                size.y);
        
        btnOctagon.setBackground(new Color(0,0,0,0));
        btnOctagon.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createLineBorder(Color.BLUE, 2),
            BorderFactory.createEmptyBorder(5, 5, 5, 5)
        ));
        
        btnOctagon.addActionListener(e -> {
            btnOctagon.setText(!octagon.getStateAnimation() ? "Stop animation" : "Start animation");
            octagon.setStateAnimation(!octagon.getStateAnimation());
        });
        
        m_render.add(btnOctagon);
        
        //triangle
        CShapeBase triangle = new CTriangle();
        triangle.setSize(new CVector(100, 80));        
        triangle.setPosition(new CVector(900, 150));
        triangle.setStyle(new CShapeStyle(Color.RED, Color.BLACK, 2));

        m_render.addShape(triangle);
        
        //triangle button
        JButton btnTriangleColor = new JButton("Change color");
        btnTriangleColor.setName("btn_triangle_color");
        btnTriangleColor.setBounds(850, 240, 200,30);
        btnTriangleColor.setBackground(Color.white);
        
        
        btnTriangleColor.addActionListener(e -> {
            if (triangle != null) {
                Color current = triangle.getStyle().Color;
                Color next = current.equals(Color.RED) ? Color.BLUE : Color.RED;
                triangle.setStyle(new CShapeStyle(next, Color.BLACK, 2));
                m_render.repaint();
            }
        });
        m_render.add(btnTriangleColor);
        
        //init render
        this.add(m_render);
    
        Runnable task = () -> {
            try {
                this.update();
            } catch(InterruptedException e) {
                Log.error(this, e.getMessage());
            }
        };
        
        m_thread = new Thread(task);
        m_thread.start();
    }
    
