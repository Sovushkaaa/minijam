    private void initKeyListener() {
        InputMap im = m_render.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW);
        ActionMap am = m_render.getActionMap();
        
        im.put(KeyStroke.getKeyStroke("ESCAPE"), "exitAction");
        
        am.put("exitAction", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                CMainForm.this.dispatchEvent(new WindowEvent(CMainForm.this, WindowEvent.WINDOW_CLOSING));  
            }
        });
    }
